def numIslands(grid: List[List[str]]) -> int:
    rows, cols = len(grid), len(grid[0])
    res = 0
    directions = [(-1,0), (1,0), (0, -1), (0,1)]
    def bfs(i, j):
        queue = deque([(i, j)])
        grid[i][j] = "#"
        while queue:
            p, q = queue.popleft()
            
            for di, dj in directions:
                ni, nj = p+di, q+dj
                if 0 <= ni < rows and 0 <= nj < cols and grid[ni][nj] == "1":
                    grid[ni][nj] = '#'   
                    queue.append((ni,nj))
    for i in range(rows):
        for j in range(cols):
            print(f"\ni: {i}, j: {j}")

            if grid[i][j] == "1":
                bfs(i,j)
                res += 1

            print(f"numberIslands: {res}")
            print_matrix(grid)
            
    return res
